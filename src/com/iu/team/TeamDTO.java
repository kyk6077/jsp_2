package com.iu.team;

public class TeamDTO {
	private String classmate;
	private int grade;
	private int ban;
	
	public String getClassmate() {
		return classmate;
	}
	public void setClassmate(String classmate) {
		this.classmate = classmate;
	}
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	public int getBan() {
		return ban;
	}
	public void setBan(int ban) {
		this.ban = ban;
	}
	
}
